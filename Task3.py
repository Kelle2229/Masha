# предмет = 'математика'
# Ожидаемый результат:
# ['Иван', 'Петр']

list_students = {
    'Иван': ['математика', 'физика'],
    'Мария': ['химия', 'биология'],
    'Петр': ['математика', 'информатика'],
    'Анна': ['биология', 'физика']
    }
find_value = "математика"
find_list = []

for key, value in list_students.items():
    if find_value in value:
        find_list.append(key)
print(find_list)
